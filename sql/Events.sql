CREATE TABLE events (
    event_id int(9) NOT NULL AUTO_INCREMENT,
    eventname varchar(30) NOT NULL,
    day int(4) NOT NULL,
    month varchar(30) NOT NULL,
    year int(6) NOT NULL,
    hour int(9) NOT NULL,
    minutes int(4) NOT NULL,
    no of people(9) NOT NULL
    description Text NOT NULL
    PRIMARY KEY (event_id)
) ENGINE=MyISAM
